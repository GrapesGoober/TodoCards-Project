<h3>FrontEnd</h3>
<p>
    This skeleton template contains a basic frontend, made in svelte.
    Visit <a href="https://kit.svelte.dev">kit.svelte.dev</a> to read the documentation
    Check out <a href="https://svelte.dev/examples/reactive-assignments">Examples</a> too!
    To all codes will be located in "src" folder
    <li>"lib" contains .js files, i.e. logic and functions for our frontend</li>
    <li>"src" contains .svelte files, i.e. html-like scripts for displaying stuff</li>
    <li>every other files are auto generated (i have no idea what they do)</li>
</p>
